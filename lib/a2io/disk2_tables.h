/*
 * DOS 3.3 Sector Interleave Tables
 *
 * The "physical sector" is the sector number encoded in the address field on disk.
 * The "logical sector" is the index within a DSK/DO file for that track.
 */

/* Physical sector -> DOS 3.3 logical sector */
static const uint8_t dos33_phys_to_logical[16] = {
    0x00, 0x07, 0x0E, 0x06, 0x0D, 0x05, 0x0C, 0x04,
    0x0B, 0x03, 0x0A, 0x02, 0x09, 0x01, 0x08, 0x0F
};

